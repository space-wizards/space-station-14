using System;
using System.Numerics;
using Content.Client.UserInterface.Controls;
using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Input;
using Robust.Shared.IoC;
using Robust.Shared.Map;

namespace Content.MapEditor.UI;

[GenerateTypedNameReferences]
public sealed partial class MapEditorScreen : UIScreen
{
    public MenuBar.Menu FileMenu { get; }
    public MenuBar.Menu EditMenu { get; }
    public MenuBar.Menu ViewMenu { get; }

    // Menu items exposed for MapEditorState to wire up events.
    public MenuBar.MenuButton FileOpenButton { get; }
    public MenuBar.MenuButton FileSaveButton { get; }
    public MenuBar.MenuButton FileExitButton { get; }
    public MenuBar.MenuButton EditUndoButton { get; }
    public MenuBar.MenuButton EditRedoButton { get; }
    public MenuBar.MenuButton ViewResetZoomButton { get; }

    /// <summary>
    ///     Raised when the user scrolls the mouse wheel over the viewport.
    ///     The float parameter is the scroll delta (positive = up/zoom in).
    /// </summary>
    public event Action<float>? OnViewportScroll;

    /// <summary>
    ///     Raised when the user presses the left mouse button over the viewport.
    /// </summary>
    public event Action<ScreenCoordinates>? OnViewportLeftDown;

    /// <summary>
    ///     Raised when the user releases the left mouse button over the viewport.
    /// </summary>
    public event Action<ScreenCoordinates>? OnViewportLeftUp;

    public MapEditorScreen()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        // Fill the entire window.
        SetAnchorPreset(RootLayout, LayoutPreset.Wide);
        SetAnchorPreset(MainLayout, LayoutPreset.Wide);
        SetAnchorPreset(ViewportContainer, LayoutPreset.Wide);
        SetAnchorPreset(MainViewport, LayoutPreset.Wide);

        // Initialize the viewport size (normally done by ViewportUIController on gameplay load).
        // 21x15 tiles at 32 pixels/meter is the standard SS14 viewport.
        var vpSize = (EyeManager.PixelsPerMeter * 21, EyeManager.PixelsPerMeter * 15);
        MainViewport.Viewport.ViewportSize = vpSize;
        MainViewport.Viewport.HorizontalExpand = true;
        MainViewport.Viewport.VerticalExpand = true;

        // Add a transparent overlay control to intercept scroll-wheel events over the viewport.
        var scrollInterceptor = new ViewportScrollInterceptor(this);
        SetAnchorPreset(scrollInterceptor, LayoutPreset.Wide);
        ViewportContainer.AddChild(scrollInterceptor);

        // Build the menu bar in code (MenuBar doesn't support XAML item declaration).
        var menuBar = new MenuBar();

        FileOpenButton = new MenuBar.MenuButton { Text = "Open Map..." };
        FileSaveButton = new MenuBar.MenuButton { Text = "Save Map..." };
        FileExitButton = new MenuBar.MenuButton { Text = "Exit" };
        FileMenu = new MenuBar.Menu
        {
            Title = "File",
            Entries =
            {
                FileOpenButton,
                FileSaveButton,
                new MenuBar.MenuSeparator(),
                FileExitButton,
            }
        };

        EditUndoButton = new MenuBar.MenuButton { Text = "Undo" };
        EditRedoButton = new MenuBar.MenuButton { Text = "Redo" };
        EditMenu = new MenuBar.Menu
        {
            Title = "Edit",
            Entries =
            {
                EditUndoButton,
                EditRedoButton,
            }
        };

        ViewResetZoomButton = new MenuBar.MenuButton { Text = "Reset Zoom" };
        ViewMenu = new MenuBar.Menu
        {
            Title = "View",
            Entries =
            {
                ViewResetZoomButton,
            }
        };

        menuBar.Menus.Add(FileMenu);
        menuBar.Menus.Add(EditMenu);
        menuBar.Menus.Add(ViewMenu);

        MenuBarContainer.AddChild(menuBar);
    }

    internal void RaiseViewportScroll(float delta)
    {
        OnViewportScroll?.Invoke(delta);
    }

    internal void RaiseViewportLeftDown(ScreenCoordinates pos)
    {
        OnViewportLeftDown?.Invoke(pos);
    }

    internal void RaiseViewportLeftUp(ScreenCoordinates pos)
    {
        OnViewportLeftUp?.Invoke(pos);
    }

    /// <summary>
    ///     Update the coordinate display in the status bar.
    /// </summary>
    public void SetStatusCoords(string text)
    {
        StatusCoords.Text = text;
    }

    /// <summary>
    ///     Update the zoom display in the status bar.
    /// </summary>
    public void SetStatusZoom(string text)
    {
        StatusZoom.Text = text;
    }

    /// <summary>
    ///     Update the active tool display in the status bar.
    /// </summary>
    public void SetStatusTool(string toolName)
    {
        StatusTool.Text = $"Tool: {toolName}";
    }

    /// <summary>
    ///     Update the general info label in the status bar.
    /// </summary>
    public void SetStatusInfo(string text)
    {
        StatusInfo.Text = text;
    }

    /// <summary>
    ///     Transparent control placed over the viewport to intercept mouse wheel events.
    /// </summary>
    private sealed class ViewportScrollInterceptor : Control
    {
        private readonly MapEditorScreen _screen;

        public ViewportScrollInterceptor(MapEditorScreen screen)
        {
            _screen = screen;
            MouseFilter = MouseFilterMode.Stop;
            HorizontalExpand = true;
            VerticalExpand = true;
        }

        protected override void MouseWheel(GUIMouseWheelEventArgs args)
        {
            base.MouseWheel(args);
            _screen.RaiseViewportScroll(args.Delta.Y);
            args.Handle();
        }

        protected override void KeyBindDown(GUIBoundKeyEventArgs args)
        {
            if (args.Function == EngineKeyFunctions.UIClick)
            {
                _screen.RaiseViewportLeftDown(args.PointerLocation);
                args.Handle();
                return;
            }

            // Let other key binds pass through to the viewport underneath.
        }

        protected override void KeyBindUp(GUIBoundKeyEventArgs args)
        {
            if (args.Function == EngineKeyFunctions.UIClick)
            {
                _screen.RaiseViewportLeftUp(args.PointerLocation);
                args.Handle();
                return;
            }

            // Pass through.
        }
    }
}
