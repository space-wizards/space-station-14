    // Tile definitions cached for filtering (includes icon texture when available).
    private readonly List<(string Name, int TileId, Texture? Icon)> _allTileDefs = new();

    // Entity prototypes cached for filtering (includes sprite icon when available).
    private readonly List<(string Display, string ProtoId, Texture? Icon)> _allEntityProtos = new();

    // Maximum entity results shown in the list for performance.
    private const int MaxEntityResults = 500;

    // Grid tab buttons keyed by grid EntityUid.
    private readonly Dictionary<EntityUid, Button> _gridTabButtons = new();
    private Button? _addGridButton;

    /// <summary>
    ///     Number of grid tabs currently shown.
    /// </summary>
    public int GridTabCount => _gridTabButtons.Count;

    public MapEditorScreen()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        // Fill the entire window.
        SetAnchorPreset(RootLayout, LayoutPreset.Wide);
        SetAnchorPreset(MainLayout, LayoutPreset.Wide);
        SetAnchorPreset(ViewportContainer, LayoutPreset.Wide);
        SetAnchorPreset(MainViewport, LayoutPreset.Wide);

        // Set initial viewport size and update it when the control resizes
        // so the render target always matches the available pixel space (no black bars).
        MainViewport.Viewport.HorizontalExpand = true;
        MainViewport.Viewport.VerticalExpand = true;
        MainViewport.OnResized += UpdateViewportSize;
        // Set a reasonable initial size (will be overridden on first layout).
        MainViewport.Viewport.ViewportSize = (EyeManager.PixelsPerMeter * 21, EyeManager.PixelsPerMeter * 15);

        // Add a transparent overlay control to intercept scroll-wheel events over the viewport.
        var scrollInterceptor = new ViewportScrollInterceptor(this);
        SetAnchorPreset(scrollInterceptor, LayoutPreset.Wide);
        ViewportContainer.AddChild(scrollInterceptor);

        // Build the menu bar in code (MenuBar doesn't support XAML item declaration).
        var menuBar = new MenuBar();

        FileOpenButton = new MenuBar.MenuButton { Text = "Open Map..." };
        FileSaveButton = new MenuBar.MenuButton { Text = "Save Map..." };
        FileExitButton = new MenuBar.MenuButton { Text = "Exit" };
        FileMenu = new MenuBar.Menu
        {
            Title = "File",
            Entries =
            {
                FileOpenButton,
                FileSaveButton,
                new MenuBar.MenuSeparator(),
                FileExitButton,
            }
        };

        EditUndoButton = new MenuBar.MenuButton { Text = "Undo" };
        EditRedoButton = new MenuBar.MenuButton { Text = "Redo" };
        EditMenu = new MenuBar.Menu
        {
            Title = "Edit",
            Entries =
            {
                EditUndoButton,
                EditRedoButton,
            }
        };

        ViewResetZoomButton = new MenuBar.MenuButton { Text = "Reset Zoom" };
        ViewMenu = new MenuBar.Menu
        {
            Title = "View",
            Entries =
            {
                ViewResetZoomButton,
            }
        };

        menuBar.Menus.Add(FileMenu);
        menuBar.Menus.Add(EditMenu);
        menuBar.Menus.Add(ViewMenu);

        MenuBarContainer.AddChild(menuBar);

        // Build toolbar buttons with descriptive labels and keyboard shortcut tooltips.
        AddToolButton("paint", "Paint", "Paint tiles (B)");
        AddToolButton("erase", "Erase", "Erase tiles (E)");
        AddToolButton("eyedropper", "Pick", "Eyedropper / pick tile (I)");
        AddToolButton("fill", "Fill", "Flood fill (F)");
        AddToolButton("rectangle", "Rect", "Rectangle draw (R)");
        AddToolButton("line", "Line", "Line draw (L)");
        AddToolButton("circle", "Circle", "Circle draw (C)");
        AddToolButton("select", "Select", "Select region (S)");
        AddToolButton("entityplace", "E.Place", "Place entities (P)");
        AddToolButton("entityselect", "E.Sel", "Select entities (V)");

        // Set tab titles for the palette tabs.
        PaletteTabContainer.SetTabTitle(0, "Tiles");
        PaletteTabContainer.SetTabTitle(1, "Entities");

        // Wire tile search filtering.
        TileSearchEdit.OnTextChanged += OnTileSearchChanged;
        TileList.OnItemSelected += OnTileItemSelected;

        // Wire entity search filtering.
        EntitySearchEdit.OnTextChanged += OnEntitySearchChanged;
        EntityList.OnItemSelected += OnEntityItemSelected;

        // Add the "+" button to the grid tab bar (always present).
        _addGridButton = new Button
        {
            Text = "+",
            MinWidth = 28,
            MinHeight = 24,
            ToolTip = "Add new grid",
        };
        _addGridButton.OnPressed += _ => OnAddGridPressed?.Invoke();
        GridTabBar.AddChild(_addGridButton);
    }

    #region Grid Tabs

    /// <summary>
    ///     Replaces all grid tabs with the given list.
    /// </summary>
    public void PopulateGridTabs(List<(EntityUid Uid, string Label)> grids)
    {
        // Remove existing tab buttons (keep the "+" button).
        foreach (var (_, btn) in _gridTabButtons)
        {
            GridTabBar.RemoveChild(btn);
        }
        _gridTabButtons.Clear();

        // Add tab buttons before the "+" button.
        foreach (var (uid, label) in grids)
        {
            AddGridTabButton(uid, label);
        }
    }

    /// <summary>
    ///     Adds a single grid tab to the bar.
    /// </summary>
    public void AddGridTab(EntityUid uid, string label)
    {
        AddGridTabButton(uid, label);
    }

    private void AddGridTabButton(EntityUid uid, string label)
    {
        var button = new Button
        {
            Text = label,
            MinWidth = 60,
            MinHeight = 24,
            ToggleMode = true,
            ToolTip = $"Switch to {label} ({uid})",
        };

        var capturedUid = uid;
        button.OnPressed += _ => OnGridTabSelected?.Invoke(capturedUid);

        _gridTabButtons[uid] = button;

        // Insert before the "+" button by adding then repositioning "+" to the end.
        GridTabBar.AddChild(button);
        _addGridButton!.SetPositionInParent(GridTabBar.ChildCount - 1);
    }

    /// <summary>
    ///     Highlights the active grid tab and un-presses others.
    /// </summary>
    public void SetActiveGridTab(EntityUid activeUid)
    {
        foreach (var (uid, btn) in _gridTabButtons)
        {
            btn.Pressed = uid == activeUid;
        }
    }

    #endregion

    #region Toolbar

    private void AddToolButton(string toolKey, string label, string? tooltip = null)
    {
        var button = new Button
        {
            Text = label,
            MinWidth = 32,
            MinHeight = 32,
            ToggleMode = true,
            ToolTip = tooltip ?? (toolKey[0..1].ToUpperInvariant() + toolKey[1..]),
        };

        button.OnPressed += _ =>
        {
            OnToolSelected?.Invoke(toolKey);
        };

        _toolButtons[toolKey] = button;
        ToolbarButtons.AddChild(button);
    }

    /// <summary>
    ///     Visually highlights the active tool button and un-presses others.
    /// </summary>
    public void SetActiveToolButton(string toolKey)
    {
        foreach (var (key, btn) in _toolButtons)
        {
            btn.Pressed = key == toolKey;
        }
    }

