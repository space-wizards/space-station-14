namespace Content.MapEditor.UI;

[GenerateTypedNameReferences]
public sealed partial class MapEditorScreen : UIScreen
{
    public MenuBar.Menu FileMenu { get; }
    public MenuBar.Menu EditMenu { get; }
    public MenuBar.Menu ViewMenu { get; }

    // Menu items exposed for MapEditorState to wire up events.
    public MenuBar.MenuButton FileOpenButton { get; }
    public MenuBar.MenuButton FileSaveButton { get; }
    public MenuBar.MenuButton FileExitButton { get; }
    public MenuBar.MenuButton EditUndoButton { get; }
    public MenuBar.MenuButton EditRedoButton { get; }
    public MenuBar.MenuButton ViewResetZoomButton { get; }

    /// <summary>
    ///     Raised when the user scrolls the mouse wheel over the viewport.
    ///     The float parameter is the scroll delta (positive = up/zoom in).
    /// </summary>
    public event Action<float>? OnViewportScroll;

    /// <summary>
    ///     Raised when the user presses the left mouse button over the viewport.
    /// </summary>
    public event Action<ScreenCoordinates>? OnViewportLeftDown;

    /// <summary>
    ///     Raised when the user releases the left mouse button over the viewport.
    /// </summary>
    public event Action<ScreenCoordinates>? OnViewportLeftUp;

    /// <summary>
    ///     Raised when a toolbar tool button is clicked.
    ///     The string parameter is the tool key ("paint", "erase", "eyedropper").
    /// </summary>
    public event Action<string>? OnToolSelected;

    /// <summary>
    ///     Raised when a tile is selected in the palette.
    ///     The int parameter is the TileId.
    /// </summary>
    public event Action<int>? OnTileSelected;

    /// <summary>
    ///     Raised when a grid tab is clicked.
    ///     The EntityUid parameter is the grid entity to switch to.
    /// </summary>
    public event Action<EntityUid>? OnGridTabSelected;

    /// <summary>
    ///     Raised when the "+" button is clicked to create a new grid.
    /// </summary>
    public event Action? OnAddGridPressed;

    // Toolbar buttons keyed by tool name.
    private readonly Dictionary<string, Button> _toolButtons = new();

    // Tile definitions cached for filtering.
    private readonly List<(string Name, int TileId)> _allTileDefs = new();

    // Grid tab buttons keyed by grid EntityUid.
    private readonly Dictionary<EntityUid, Button> _gridTabButtons = new();
    private Button? _addGridButton;

    /// <summary>
    ///     Number of grid tabs currently shown.
    /// </summary>
    public int GridTabCount => _gridTabButtons.Count;

    public MapEditorScreen()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        // Fill the entire window.
        SetAnchorPreset(RootLayout, LayoutPreset.Wide);
        SetAnchorPreset(MainLayout, LayoutPreset.Wide);
        SetAnchorPreset(ViewportContainer, LayoutPreset.Wide);
        SetAnchorPreset(MainViewport, LayoutPreset.Wide);

        // Initialize the viewport size (normally done by ViewportUIController on gameplay load).
        // 21x15 tiles at 32 pixels/meter is the standard SS14 viewport.
        var vpSize = (EyeManager.PixelsPerMeter * 21, EyeManager.PixelsPerMeter * 15);
        MainViewport.Viewport.ViewportSize = vpSize;
        MainViewport.Viewport.HorizontalExpand = true;
        MainViewport.Viewport.VerticalExpand = true;

        // Add a transparent overlay control to intercept scroll-wheel events over the viewport.
        var scrollInterceptor = new ViewportScrollInterceptor(this);
        SetAnchorPreset(scrollInterceptor, LayoutPreset.Wide);
        ViewportContainer.AddChild(scrollInterceptor);

        // Build the menu bar in code (MenuBar doesn't support XAML item declaration).
        var menuBar = new MenuBar();

        FileOpenButton = new MenuBar.MenuButton { Text = "Open Map..." };
        FileSaveButton = new MenuBar.MenuButton { Text = "Save Map..." };
        FileExitButton = new MenuBar.MenuButton { Text = "Exit" };
        FileMenu = new MenuBar.Menu
        {
            Title = "File",
            Entries =
            {
                FileOpenButton,
                FileSaveButton,
                new MenuBar.MenuSeparator(),
                FileExitButton,
            }
        };

        EditUndoButton = new MenuBar.MenuButton { Text = "Undo" };
        EditRedoButton = new MenuBar.MenuButton { Text = "Redo" };
        EditMenu = new MenuBar.Menu
        {
            Title = "Edit",
            Entries =
            {
                EditUndoButton,
                EditRedoButton,
            }
        };

        ViewResetZoomButton = new MenuBar.MenuButton { Text = "Reset Zoom" };
        ViewMenu = new MenuBar.Menu
        {
            Title = "View",
            Entries =
            {
                ViewResetZoomButton,
            }
        };

        menuBar.Menus.Add(FileMenu);
        menuBar.Menus.Add(EditMenu);
        menuBar.Menus.Add(ViewMenu);

        MenuBarContainer.AddChild(menuBar);

        // Build toolbar buttons.
        AddToolButton("paint", "P");
        AddToolButton("erase", "E");
        AddToolButton("eyedropper", "I");
        AddToolButton("fill", "F");
        AddToolButton("rectangle", "R");
        AddToolButton("line", "L");
        AddToolButton("circle", "C");

        // Wire tile search filtering.
        TileSearchEdit.OnTextChanged += OnTileSearchChanged;
        TileList.OnItemSelected += OnTileItemSelected;

        // Add the "+" button to the grid tab bar (always present).
        _addGridButton = new Button
        {
            Text = "+",
            MinWidth = 28,
            MinHeight = 24,
            ToolTip = "Add new grid",
        };
        _addGridButton.OnPressed += _ => OnAddGridPressed?.Invoke();
        GridTabBar.AddChild(_addGridButton);
    }

    #region Grid Tabs

    /// <summary>
    ///     Replaces all grid tabs with the given list.
    /// </summary>
    public void PopulateGridTabs(List<(EntityUid Uid, string Label)> grids)
    {
        // Remove existing tab buttons (keep the "+" button).
        foreach (var (_, btn) in _gridTabButtons)
        {
            GridTabBar.RemoveChild(btn);
        }
        _gridTabButtons.Clear();

        // Add tab buttons before the "+" button.
        foreach (var (uid, label) in grids)
        {
            AddGridTabButton(uid, label);
        }
    }

    /// <summary>
    ///     Adds a single grid tab to the bar.
    /// </summary>
    public void AddGridTab(EntityUid uid, string label)
    {
        AddGridTabButton(uid, label);
    }

    private void AddGridTabButton(EntityUid uid, string label)
    {
        var button = new Button
        {
            Text = label,
            MinWidth = 60,
            MinHeight = 24,
            ToggleMode = true,
            ToolTip = $"Switch to {label} ({uid})",
        };

        var capturedUid = uid;
        button.OnPressed += _ => OnGridTabSelected?.Invoke(capturedUid);

        _gridTabButtons[uid] = button;

        // Insert before the "+" button by adding then repositioning "+" to the end.
        GridTabBar.AddChild(button);
        _addGridButton!.SetPositionInParent(GridTabBar.ChildCount - 1);
    }

    /// <summary>
    ///     Highlights the active grid tab and un-presses others.
    /// </summary>
    public void SetActiveGridTab(EntityUid activeUid)
    {
        foreach (var (uid, btn) in _gridTabButtons)
        {
            btn.Pressed = uid == activeUid;
        }
    }

    #endregion

    #region Toolbar

    private void AddToolButton(string toolKey, string label)
    {
        var button = new Button
        {
            Text = label,
            MinWidth = 32,
            MinHeight = 32,
            ToggleMode = true,
            ToolTip = toolKey[0..1].ToUpperInvariant() + toolKey[1..],
        };

        button.OnPressed += _ =>
        {
            OnToolSelected?.Invoke(toolKey);
        };

        _toolButtons[toolKey] = button;
        ToolbarButtons.AddChild(button);
    }

    /// <summary>
    ///     Visually highlights the active tool button and un-presses others.
    /// </summary>
    public void SetActiveToolButton(string toolKey)
    {
        foreach (var (key, btn) in _toolButtons)
        {
            btn.Pressed = key == toolKey;
        }
    }

    #endregion

    #region Tile Palette

    /// <summary>
    ///     Populates the tile palette from the tile definition manager.
    ///     Should be called once during state startup.
    /// </summary>
    public void PopulateTilePalette(ITileDefinitionManager tileDefs)
    {
        _allTileDefs.Clear();

        foreach (var def in tileDefs)
        {
            if (def.EditorHidden)
                continue;

            // Skip the "Space" tile (ID 0) — it's not useful to paint.
            if (def.TileId == 0)
                continue;

            _allTileDefs.Add((def.Name, (int) def.TileId));
        }

        // Sort alphabetically.
        _allTileDefs.Sort((a, b) => string.Compare(a.Name, b.Name, StringComparison.OrdinalIgnoreCase));

        RefreshTileList(string.Empty);
    }

    /// <summary>
    ///     Selects the tile in the palette that matches the given tile ID.
    ///     Used when the eyedropper picks a tile.
    /// </summary>
    public void SelectTileInPalette(int tileId)
    {
        for (var i = 0; i < TileList.Count; i++)
        {
            var item = TileList[i];
            if (item.Metadata is int id && id == tileId)
            {
                item.Selected = true;
                return;
            }
        }
    }

    private void OnTileSearchChanged(LineEdit.LineEditEventArgs args)
    {
        RefreshTileList(args.Text);
    }

    private void RefreshTileList(string filter)
    {
        TileList.Clear();

        foreach (var (name, tileId) in _allTileDefs)
        {
            if (!string.IsNullOrEmpty(filter) &&
                !name.Contains(filter, StringComparison.OrdinalIgnoreCase))
            {
                continue;
            }

            TileList.AddItem(name, metadata: tileId);
        }
    }

    private void OnTileItemSelected(ItemList.ItemListSelectedEventArgs args)
    {
        var item = TileList[args.ItemIndex];
        if (item.Metadata is int tileId)
        {
            OnTileSelected?.Invoke(tileId);
        }
    }

    #endregion

    #region Viewport Events

    internal void RaiseViewportScroll(float delta)
    {
        OnViewportScroll?.Invoke(delta);
    }

    internal void RaiseViewportLeftDown(ScreenCoordinates pos)
    {
        OnViewportLeftDown?.Invoke(pos);
    }

    internal void RaiseViewportLeftUp(ScreenCoordinates pos)
    {
        OnViewportLeftUp?.Invoke(pos);
    }

    #endregion

    #region Status Bar

    /// <summary>
    ///     Update the coordinate display in the status bar.
    /// </summary>
    public void SetStatusCoords(string text)
    {
        StatusCoords.Text = text;
    }

    /// <summary>
    ///     Update the zoom display in the status bar.
    /// </summary>
    public void SetStatusZoom(string text)
    {
        StatusZoom.Text = text;
    }

    /// <summary>
    ///     Update the active tool display in the status bar.
    /// </summary>
    public void SetStatusTool(string toolName)
    {
        StatusTool.Text = $"Tool: {toolName}";
    }

    /// <summary>
    ///     Update the general info label in the status bar.
    /// </summary>
    public void SetStatusInfo(string text)
    {
        StatusInfo.Text = text;
    }

    #endregion

    /// <summary>
    ///     Transparent control placed over the viewport to intercept mouse wheel events.
    /// </summary>
    private sealed class ViewportScrollInterceptor : Control
    {
        private readonly MapEditorScreen _screen;

        public ViewportScrollInterceptor(MapEditorScreen screen)
        {
            _screen = screen;
            MouseFilter = MouseFilterMode.Stop;
            HorizontalExpand = true;
            VerticalExpand = true;
        }

        protected override void MouseWheel(GUIMouseWheelEventArgs args)
        {
            base.MouseWheel(args);
            _screen.RaiseViewportScroll(args.Delta.Y);
            args.Handle();
        }

        protected override void KeyBindDown(GUIBoundKeyEventArgs args)
        {
            Robust.Shared.Log.Logger.GetSawmill("map_editor").Debug($"Interceptor KeyBindDown: {args.Function}");
            if (args.Function == EngineKeyFunctions.UIClick
                || args.Function == EngineKeyFunctions.Use)
            {
                _screen.RaiseViewportLeftDown(args.PointerLocation);
                args.Handle();
                return;
            }
        }

        protected override void KeyBindUp(GUIBoundKeyEventArgs args)
        {
            if (args.Function == EngineKeyFunctions.UIClick
                || args.Function == EngineKeyFunctions.Use)
            {
                _screen.RaiseViewportLeftUp(args.PointerLocation);
                args.Handle();
                return;
            }
        }
    }
}
