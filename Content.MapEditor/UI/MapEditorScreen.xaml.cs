    /// <summary>
    ///     Populates the entity palette from the prototype manager.
    ///     Should be called once during state startup.
    /// </summary>
    public void PopulateEntityPalette(IPrototypeManager protoManager)
    {
        _allEntityProtos.Clear();

        var spriteSystem = IoCManager.Resolve<IEntityManager>().System<SpriteSystem>();

        foreach (var proto in protoManager.EnumeratePrototypes<EntityPrototype>())
        {
            if (proto.HideSpawnMenu || proto.Abstract)
                continue;

            // Skip entities whose categories include one with HideSpawnMenu.
            var hiddenByCategory = false;
            foreach (var cat in proto.Categories)
            {
                if (cat.HideSpawnMenu)
                {
                    hiddenByCategory = true;
                    break;
                }
            }
            if (hiddenByCategory)
                continue;

            // Display as "Name [ID]" if Name differs from ID, otherwise just ID.
            string display;
            try
            {
                var name = proto.Name;
                display = !string.IsNullOrEmpty(name) && name != proto.ID
                    ? $"{name} [{proto.ID}]"
                    : proto.ID;
            }
            catch
            {
                // Name lookup can fail if localization is not loaded.
                display = proto.ID;
            }

            // Try to get the first sprite texture for the palette icon.
            Texture? icon = null;
            try
            {
                var textures = spriteSystem.GetPrototypeTextures(proto);
                icon = textures.FirstOrDefault()?.Default;
            }
            catch
            {
                // Some prototypes may fail to resolve textures fall back to no icon.
            }

            var category = GetEntityCategory(proto, protoManager);

            _allEntityProtos.Add((display, proto.ID, icon, category));
        }

        // Sort alphabetically by prototype ID.
        _allEntityProtos.Sort((a, b) => string.Compare(a.ProtoId, b.ProtoId, StringComparison.OrdinalIgnoreCase));

        RefreshEntityList(string.Empty);
    }

    private void OnEntitySearchChanged(LineEdit.LineEditEventArgs args)
