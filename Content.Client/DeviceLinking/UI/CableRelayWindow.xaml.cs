using System.Collections.Generic;
using Content.Client.Message;
using Content.Client.UserInterface.Controls;
using Content.Shared.Power;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Maths;

namespace Content.Client.DeviceLinking.UI;

/// <summary>
/// Configuration window for the electro-relay: flip it on or off and choose which voltages it severs.
/// </summary>
[GenerateTypedNameReferences]
public sealed partial class CableRelayWindow : FancyWindow
{
    public event Action? OnTogglePressed;
    public event Action<CableType, bool>? OnCableTypeToggled;

    // Per-voltage colours matching the in-world cables; a type button is tinted while enabled, grey while disabled.
    private static readonly Color HvColor = Color.FromHex("#C27122");
    private static readonly Color MvColor = Color.FromHex("#C2B021");
    private static readonly Color LvColor = Color.FromHex("#3FA94B");

    public CableRelayWindow()
    {
        RobustXamlLoader.Load(this);

        PowerSwitch.OnToggled += _ => OnTogglePressed?.Invoke();
        ButtonHV.OnToggled += args => OnCableTypeToggled?.Invoke(CableType.HighVoltage, args.Pressed);
        ButtonMV.OnToggled += args => OnCableTypeToggled?.Invoke(CableType.MediumVoltage, args.Pressed);
        ButtonLV.OnToggled += args => OnCableTypeToggled?.Invoke(CableType.Apc, args.Pressed);
    }

    public void UpdateState(bool powered, bool severed, HashSet<CableType> affectedTypes)
    {
        PowerSwitch.Pressed = severed;
        SetTypeButton(ButtonHV, CableType.HighVoltage, HvColor, affectedTypes);
        SetTypeButton(ButtonMV, CableType.MediumVoltage, MvColor, affectedTypes);
        SetTypeButton(ButtonLV, CableType.Apc, LvColor, affectedTypes);

        StatusLabel.SetMarkup(Loc.GetString(
            !powered ? "cable-relay-menu-status-unpowered"
            : severed ? "cable-relay-menu-status-severed"
            : "cable-relay-menu-status-connected"));
    }

    private static void SetTypeButton(Button button, CableType type, Color color, HashSet<CableType> affectedTypes)
    {
        var enabled = affectedTypes.Contains(type);
        button.Pressed = enabled;
        button.ModulateSelfOverride = enabled ? color : null;
    }
}
