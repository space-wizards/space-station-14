using System.Diagnostics.CodeAnalysis;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Input;
using Robust.Shared.Random;
using Robust.Shared.Timing;
using Robust.Shared.Utility;
using static Robust.Client.UserInterface.Controls.BaseButton;
using Range = Robust.Client.UserInterface.Controls.Range;

namespace Content.Client.Instruments.UI;

[GenerateTypedNameReferences]
public sealed partial class FileMidiSource : InstrumentMidiSourceBase
{
    [Dependency] private IEntityManager _entManager = default!;
    [Dependency] private IRobustRandom _random = default!;

    /// <summary>
    /// Raised when the MIDI track with the given name should be played.
    /// </summary>
    public event Action<string>? StartPlayingRequest;

    /// <summary>
    /// Raised when any currently playing MIDI track should be stopped.
    /// </summary>
    public event Action? StopPlayingRequest;

    /// <summary>
    /// Raised when the track position on the currently playing MIDI track should change.
    /// </summary>
    public event Action<int>? TrackPositionChangeRequest;

    /// <summary>
    /// Raised when the user would like to rename the MIDI file with the given name.
    /// </summary>
    public event Action<string>? FileRenameRequest;

    /// <summary>
    /// Raised when the user would like to remove the MIDI file with the given name.
    /// </summary>
    public event Action<string>? FileRemoveRequest;

    /// <summary>
    /// Raised when the user would like to add a new MIDI file.
    /// </summary>
    public event Action? FileAddNewRequest;

    /// <summary>
    /// Raised when looping MIDI tracks is toggled.
    /// </summary>
    public event Action<bool>? LoopingToggled;

    private readonly string _noTrackSelectedText = Loc.GetString("instruments-component-menu-files-no-track-selected");
    private readonly List<string> _loadedTracks = [];
    private EntityUid? _entity;

    private bool IsShuffle => ShuffleButton.Pressed;
    private string CurrentFilter => FilterBar.Text;
    public override string ButtonName => Loc.GetString("instruments-component-menu-file-midi-source-button");

    /// <summary>
    /// Controls of MIDI tracks should currently be played. This is connected to the play button.
    /// </summary>
    public bool IsPlaying
    {
        get => PlayButton.Pressed;
        set => PlayButton.Pressed = value;
    }

    public FileMidiSource()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        CurrentTrackLabel.Text = _noTrackSelectedText;

        FilterBar.OnTextChanged += OnFilterBarTextChanged;
        TrackList.OnItemSelected += OnTrackListItemSelected;
        AddButton.OnPressed += OnAddButtonPressed;
        RenameButton.OnPressed += OnRenameButtonPressed;
        RemoveButton.OnPressed += OnRemoveButtonPressed;
        PlayButton.OnToggled += OnPlayButtonToggled;
        LoopButton.OnToggled += OnLoopButtonToggled;
        PlaybackSlider.OnValueChanged += OnPlaybackSliderValueChanged;
        PlaybackSlider.OnKeyBindUp += OnPlaybackSliderKeyBindUp;

        AddButton.TooltipSupplier = SupplyFileManagementTooltip;
        RenameButton.TooltipSupplier = SupplyFileManagementTooltip;
        RemoveButton.TooltipSupplier = SupplyFileManagementTooltip;
    }

    protected override void FrameUpdate(FrameEventArgs args)
    {
        base.FrameUpdate(args);

        if (!_entManager.TryGetComponent(_entity, out InstrumentComponent? instrument))
            return;

        if (PlaybackSlider.Grabbed)
            return;

        if (!instrument.IsMidiOpen)
        {
            ResetTrackIndicators();
            PlaybackSlider.Disabled = true;
        }
        else if (instrument.IsRendererAlive)
        {
            PlaybackSlider.Disabled = false;
            PlaybackSlider.MaxValue = instrument.PlayerTotalTick;
            PlaybackSlider.SetValueWithoutEvent(instrument.PlayerTick);
            // TODO: SequencerTimeScale does not return the actual ticks/seconds, find solution.
            var totalTime = TimeSpan.FromSeconds(Math.Ceiling(instrument.PlayerTotalTick / instrument.Renderer!.SequencerTimeScale));
            var currentTime = TimeSpan.FromSeconds(Math.Ceiling(instrument.PlayerTick / instrument.Renderer!.SequencerTimeScale));
            TimeLabel.Text = totalTime.Hours < 1
                ? Loc.GetString(
                    "instruments-component-menu-files-track-playtime-seconds-minutes",
                    ("currentMinutes", currentTime.ToString("mm")),
                    ("currentSeconds", currentTime.ToString("ss")),
                    ("totalMinutes", totalTime.ToString("mm")),
                    ("totalSeconds", totalTime.ToString("ss")))
                : Loc.GetString(
                    "instruments-component-menu-files-track-playtime-seconds-minutes-hours",
                    ("currentHours", currentTime.ToString("hh")),
                    ("currentMinutes", currentTime.ToString("mm")),
                    ("currentSeconds", currentTime.ToString("ss")),
                    ("totalHours", totalTime.ToString("hh")),
                    ("totalMinutes", totalTime.ToString("mm")),
                    ("totalSeconds", totalTime.ToString("ss")));
        }
    }

    public override void Disable()
    {
        base.Disable();
        TrackList.ClearSelected();
        IsPlaying = false;
        StopPlayingRequest?.Invoke();
        CurrentTrackLabel.Text = _noTrackSelectedText;
    }

    private void OnFilterBarTextChanged(LineEdit.LineEditEventArgs obj)
    {
        FilterTrackList();
    }

    private void OnTrackListItemSelected(ItemList.ItemListSelectedEventArgs obj)
    {
        var currentItem = obj.ItemList[obj.ItemIndex];

        if (string.IsNullOrEmpty(currentItem.Text))
            return;

        CurrentTrackLabel.Text = currentItem.Text;

        if (!IsPlaying)
            return;

        StartPlaying(currentItem.Text);
    }

    private void OnAddButtonPressed(ButtonEventArgs obj)
    {
        FileAddNewRequest?.Invoke();
    }

    private void OnRenameButtonPressed(ButtonEventArgs obj)
    {
        if (!TryGetSelectedTrackName(out var name))
            return;

        FileRenameRequest?.Invoke(name);
    }

    private void OnRemoveButtonPressed(ButtonEventArgs obj)
    {
        if (!TryGetSelectedTrackName(out var name))
            return;

        FileRemoveRequest?.Invoke(name);
    }

    private void OnPlayButtonToggled(ButtonToggledEventArgs obj)
    {
        if (PlayButton.Pressed)
        {
            DisableFileManagement();
            if (!TryGetSelectedTrackName(out var trackName))
                return;

            StartPlaying(trackName);
        }
        else
        {
            EnableFileManagement();
            StopPlaying();
        }
    }

    private void OnLoopButtonToggled(ButtonToggledEventArgs obj)
    {
        LoopingToggled?.Invoke(LoopButton.Pressed);
    }

    private void OnPlaybackSliderValueChanged(Range _)
    {
        // Do not seek while still grabbing.
        if (PlaybackSlider.Grabbed)
            return;

        TrackPositionChangeRequest?.Invoke((int)Math.Ceiling(PlaybackSlider.Value));
    }

    private void OnPlaybackSliderKeyBindUp(GUIBoundKeyEventArgs obj)
    {
        if (obj.Function != EngineKeyFunctions.UIClick)
            return;

        TrackPositionChangeRequest?.Invoke((int)Math.Ceiling(PlaybackSlider.Value));
    }

    private Control? SupplyFileManagementTooltip(Control? sender)
    {
        if (sender is not Button { Disabled: true })
            return null;

        var tooltipText = Loc.GetString("instruments-component-menu-files-file-management-disabled-info");
        var tooltip = new Tooltip();
        tooltip.SetMessage(FormattedMessage.FromUnformatted(tooltipText));
        return tooltip;
    }

    private void DisableFileManagement()
    {
        RemoveButton.Disabled = true;
        RenameButton.Disabled = true;
        AddButton.Disabled = true;
    }

    private void EnableFileManagement()
    {
        RemoveButton.Disabled = false;
        RenameButton.Disabled = false;
        AddButton.Disabled = false;
    }

    private void ResetTrackIndicators()
    {
        PlaybackSlider.MaxValue = 1;
        PlaybackSlider.SetValueWithoutEvent(0);
        TimeLabel.Text = "--.--/--.--";
    }

    private void StopPlaying()
    {
        StopPlayingRequest?.Invoke();
        ResetTrackIndicators();
    }

    private void StartPlaying(string fileName)
    {
        StartPlayingRequest?.Invoke(fileName);
    }

    private void FilterTrackList()
    {
        var filterString = CurrentFilter.Trim().ToLowerInvariant();
        TrackList.Clear();

        foreach (var track in _loadedTracks)
        {
            var trackName = track.Trim().ToLowerInvariant();
            if(trackName.Contains(filterString))
                TrackList.AddItem(track);
        }
    }

    private bool TryGetSelectedTrackName([NotNullWhen(true)] out string? trackName)
    {
        trackName = null;
        if (!TryGetSelectedTrack(out var item) || item.Text is not { Length: > 0 })
            return false;

        trackName = item.Text;
        return true;
    }

    private bool TryGetSelectedTrack([NotNullWhen(true)] out ItemList.Item? item)
    {
        return TrackList.GetSelected().TryFirstOrDefault(out item);
    }

    /// <summary>
    /// Set the instrument entity actually playing the tracks. This is needed for the UI to read out current track time
    /// on <see cref="FrameUpdate"/>.
    /// </summary>
    /// <param name="ent">The instrument entity playing the tracks.</param>
    public void SetEntity(EntityUid ent)
    {
        _entity = ent;
    }

    /// <summary>
    /// Replaces the current MIDI tracks in the list with the passed ones.
    /// </summary>
    /// <param name="tracks">The tracks to add.</param>
    public void PopulateTrackList(IEnumerable<string> tracks)
    {
        _loadedTracks.Clear();
        _loadedTracks.AddRange(tracks);
        FilterTrackList();
    }

    /// <summary>
    /// Adds the passed MIDI track to the list.
    /// </summary>
    /// <param name="name">Name of the MIDI track to add.</param>
    public void AddTrack(string name)
    {
        _loadedTracks.Add(name);
        FilterTrackList();
    }

    /// <summary>
    /// Removes the passed MIDI track from the list.
    /// </summary>
    /// <param name="name">Name of the MIDI track to remove.</param>
    public void RemoveTrack(string name)
    {
        _loadedTracks.Remove(name);
        FilterTrackList();
    }

    /// <summary>
    /// Updates the name of a MIDI track inside the list.
    /// </summary>
    /// <param name="currentName">Current name of the MIDI track.</param>
    /// <param name="newName">New name for the MIDI track.</param>
    public void UpdateTrackName(string currentName, string newName)
    {
        if (_loadedTracks.Remove(currentName))
            _loadedTracks.Add(newName);
        FilterTrackList();
    }

    /// <summary>
    /// Selects the next MIDI track from the list according to shuffle and filter settings.
    /// </summary>
    /// <remarks>Selecting another track causes its playback if <see cref="IsPlaying"/> is true.</remarks>
    public void SelectNextTrack()
    {
        // Only proceed if panel is visible.
        if (!Visible)
            return;

        // Only proceed if the play button is pressed.
        if (!IsPlaying)
            return;

        // List is empty, return
        if (TrackList.Count == 0)
            return;

        if (IsShuffle)
        {
            TrackList[_random.Next(0, TrackList.Count)].Selected = true;
        }
        else
        {
            if (!TryGetSelectedTrack(out var item))
            {
                TrackList[0].Selected = true;
                return;
            }

            for (var i = 0; i < TrackList.Count - 1; i++)
            {
                if (TrackList[i] != item)
                    continue;
                TrackList[i + 1].Selected = true;
                return;
            }
        }
    }
}
