using Content.Shared.CartridgeLoader.Cartridges;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Utility;

namespace Content.Client.CartridgeLoader.Cartridges;

[GenerateTypedNameReferences]
public sealed partial class MessengerUiFragment : BoxContainer
{
    public event Action<string>? OnContactSelected;
    public event Action? OnBackPressed;
    public event Action<string, string>? OnMessageSent;

    private string? _activeContact;
    private string _currentOwner = "";

    public MessengerUiFragment()
    {
        RobustXamlLoader.Load(this);
        Orientation = LayoutOrientation.Vertical;
        HorizontalExpand = true;
        VerticalExpand = true;

        BackButton.OnPressed += _ => OnBackPressed?.Invoke();

        SendButton.OnPressed += _ => SendCurrentMessage();

        MessageInput.OnTextEntered += _ => SendCurrentMessage();
    }

    private void SendCurrentMessage()
    {
        var text = MessageInput.Text.Trim();
        if (string.IsNullOrEmpty(text) || _activeContact == null)
            return;

        OnMessageSent?.Invoke(_activeContact, text);
        MessageInput.Clear();
    }

    public void UpdateState(MessengerUiState state)
    {
        _currentOwner = state.CurrentOwnerName;
        var previousContact = _activeContact;
        _activeContact = state.ActiveContactName;

        if (_activeContact != null)
        {
            ContactListView.Visible = false;
            ChatView.Visible = true;
            ChatTitle.Text = _activeContact;
            UpdateMessages(state.ActiveMessages);

            if (previousContact != _activeContact)
                MessageInput.GrabKeyboardFocus();
        }
        else
        {
            ContactListView.Visible = true;
            ChatView.Visible = false;
            UpdateContacts(state.Contacts);
        }
    }

    private void UpdateContacts(List<MessengerContact> contacts)
    {
        ContactList.RemoveAllChildren();

        if (contacts.Count == 0)
        {
            var empty = new Label();
            empty.Text = Loc.GetString("messenger-ui-no-contacts");
            empty.HorizontalExpand = true;
            empty.Align = Label.AlignMode.Center;
            empty.Margin = new Thickness(4, 20, 4, 4);
            ContactList.AddChild(empty);
            return;
        }

        foreach (var contact in contacts)
        {
            var row = new BoxContainer();
            row.Orientation = LayoutOrientation.Horizontal;
            row.HorizontalExpand = true;
            row.Margin = new Thickness(2);

            var button = new Button();
            button.HorizontalExpand = true;

            var nameText = contact.Name;
            if (contact.JobTitle != null)
                nameText += $" ({contact.JobTitle})";
            if (contact.HasUnread)
                nameText = $"* {nameText}";

            button.Text = nameText;

            var capturedName = contact.Name;
            button.OnPressed += _ => OnContactSelected?.Invoke(capturedName);

            row.AddChild(button);
            ContactList.AddChild(row);
        }
    }

    private void UpdateMessages(List<MessengerMessageData> messages)
    {
        MessageList.RemoveAllChildren();

        foreach (var msg in messages)
        {
            var isOwn = msg.Sender == _currentOwner;

            var row = new BoxContainer();
            row.Orientation = LayoutOrientation.Vertical;
            row.HorizontalExpand = true;
            row.Margin = new Thickness(4, 2, 4, 2);

            var header = new Label();
            header.Text = msg.Sender;
            header.FontColorOverride = isOwn ? Color.FromHex("#4a9eff") : Color.FromHex("#55cc55");

            var contentMessage = new FormattedMessage();
            contentMessage.AddText(msg.Content);

            var content = new RichTextLabel();
            content.HorizontalExpand = true;
            content.SetMessage(contentMessage);
            content.Margin = new Thickness(8, 0, 0, 2);

            row.AddChild(header);
            row.AddChild(content);
            MessageList.AddChild(row);
        }

        MessageScroll.VScrollTarget = float.MaxValue;
    }
}