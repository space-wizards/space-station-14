using Content.Client.Chat;
using Content.Client.UserInterface.Controls;
using Content.Shared.Administration;
using Robust.Client.AutoGenerated;
using Robust.Client.Player;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.Options;

[GenerateTypedNameReferences]
public sealed partial class AbandonCharacterWindow : FancyWindow
{
    [Dependency] private readonly IPlayerManager _playerManager = default!;
    [Dependency] private readonly IEntityManager _entityManager = default!;

    public AbandonCharacterWindow()
    {
        IoCManager.InjectDependencies(this);
        RobustXamlLoader.Load(this);

        SuicideButton.OnPressed += _ => Close();
        GhostButton.OnPressed += _ => Close();
        CancelButton.OnPressed += _ => Close();

        var player = _playerManager.LocalEntity;
        if (player == null)
            return;

        SuicideButton.Disabled = !_entityManager.System<SuicideSystem>().CanSuicide(player.Value);
        GhostButton.Disabled = _entityManager.HasComponent<AdminFrozenComponent>(player.Value);
    }
}
