using Content.Shared._FinalStand.Shop;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client._FinalStand.Shop.UI;

[GenerateTypedNameReferences]
public sealed partial class WeaponShopWindow : DefaultWindow
{
    [Dependency] private readonly IEntityManager _entityManager = default!;

    public event Action? OnBuyPressed;
    public event Action<string>? OnUpgradePressed;

    private int _price;

    public WeaponShopWindow()
    {
        IoCManager.InjectDependencies(this);
        RobustXamlLoader.Load(this);
        BuyButton.OnPressed += _ => OnBuyPressed?.Invoke();
    }

    public void Populate(EntityUid shopEntity, IEntityManager entMan)
    {
        if (!entMan.TryGetComponent<FSShopWeaponComponent>(shopEntity, out var comp))
            return;

        _price = comp.Price;

        var meta = entMan.GetComponent<MetaDataComponent>(shopEntity);
        Title = "Weapon Shop";
        WeaponNameLabel.Text = Capitalize(meta.EntityName);
        WeaponNameLabel.Modulate = Color.FromHex("#F4C430");
        DescLabel.SetMessage(meta.EntityDescription);
        StatsLabel.Text = BuildStats();

        PriceLabel.Text = $"${_price:N0}";
        PriceLabel.Modulate = Color.FromHex("#EBCB8B");
        BuyButton.Text = $"Buy for ${_price:N0}";

        var shopClient = _entityManager.System<FSShopClientSystem>();
        RefreshUpgrades(comp.Upgrades, shopClient.UpgradeLevels, shopClient.CurrentCredits);
        UpdateBalance(shopClient.CurrentCredits);
    }

    public void UpdateBalance(int credits)
    {
        BalanceLabel.Text = $"${credits:N0}";
        var canAfford = credits >= _price;
        BuyButton.Disabled = !canAfford;
        BalanceLabel.Modulate = canAfford ? Color.LimeGreen : Color.OrangeRed;
    }

    public void RefreshUpgrades(List<WeaponUpgradeDef> defs, Dictionary<string, int> levels, int credits)
    {
        UpgradesContainer.RemoveAllChildren();

        if (defs.Count == 0)
        {
            var placeholder = new Label { Text = "No upgrades available.", Modulate = Color.FromHex("#8FA1B3") };
            UpgradesContainer.AddChild(placeholder);
            return;
        }

        foreach (var def in defs)
        {
            var level = levels.GetValueOrDefault(def.Id, 0);
            UpgradesContainer.AddChild(BuildUpgradeRow(def, level, credits));
        }
    }
    private static string Capitalize(string text)
    {
        if (text.Length == 0)
            return text;

        return text.Substring(0, 1).ToUpper() + text.Substring(1);
    }
    private BoxContainer BuildUpgradeRow(WeaponUpgradeDef def, int currentLevel, int credits)
    {
        var row = new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Horizontal,
            Margin = new Thickness(0, 0, 0, 6),
        };

        var atMax = currentLevel >= def.MaxLevel;
        var nextCost = def.BaseCost * (currentLevel + 1);

        // Name button — clicking purchases next level
        var btn = new Button
        {
            Text = def.Name,
            MinWidth = 130,
            Disabled = atMax || credits < nextCost,
            ToolTip = def.Description,
        };
        btn.OnPressed += _ => OnUpgradePressed?.Invoke(def.Id);
        row.AddChild(btn);

        // Level squares
        var squares = new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Horizontal,
            Margin = new Thickness(6, 0, 6, 0),
        };
        for (var i = 0; i < def.MaxLevel; i++)
        {
            var sq = new Label
            {
                Text = "■",
                Modulate = i < currentLevel
                    ? Color.FromHex("#A3BE8C")
                    : Color.FromHex("#434C5E"),
            };
            squares.AddChild(sq);
        }
        row.AddChild(squares);

        // Cost / MAX label
        var costLabel = new Label
        {
            Text = atMax ? "MAX" : $"${nextCost:N0}",
            Modulate = atMax ? Color.FromHex("#A3BE8C") : Color.FromHex("#EBCB8B"),
            HorizontalExpand = true,
            HorizontalAlignment = HAlignment.Right,
        };
        row.AddChild(costLabel);

        return row;
    }

    private static string BuildStats() => "—";
}
