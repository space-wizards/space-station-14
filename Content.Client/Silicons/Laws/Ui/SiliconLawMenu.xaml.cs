using Content.Client.UserInterface.Controls;
using Content.Shared.Radio.Components;
using Content.Shared.Silicons.Laws;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.Silicons.Laws.Ui;

[GenerateTypedNameReferences]
public sealed partial class SiliconLawMenu : FancyWindow
{
    [Dependency] private readonly IEntityManager _entity = default!;
    private readonly SharedSiliconLawSystem _lawSystem;

    public SiliconLawMenu()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        _lawSystem = _entity.System<SiliconLawSystem>();
    }

    public void Update(EntityUid uid)
    {
        var laws = _lawSystem.GetLaws(uid);
        var radioComp = _entity.GetComponent<IntrinsicRadioTransmitterComponent>(uid);
        var channels = radioComp.Channels;
        LawDisplayContainer.Children.Clear();

        foreach (var law in laws.Laws)
        {
            var control = new LawDisplay(uid, law, channels);

            LawDisplayContainer.AddChild(control);
        }
    }
}
