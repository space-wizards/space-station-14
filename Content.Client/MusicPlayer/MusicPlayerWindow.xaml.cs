using System;
using System.Collections.Generic;
using Content.Shared.MusicPlayer;
using Robust.Client.Audio;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Audio.Components;
using Robust.Shared.IoC;
using Robust.Shared.Prototypes;

namespace Content.Client.MusicPlayer;

[GenerateTypedNameReferences]
public sealed partial class MusicPlayerWindow : Content.Client.UserInterface.Controls.FancyWindow
{
    [Dependency] private readonly IEntityManager _entManager = default!;
    private AudioSystem _audioSystem = default!;

    public event Action<ProtoId<MusicCategoryPrototype>>? OnCategorySelected;
    public event Action<ProtoId<MusicTrackPrototype>>? OnTrackSelected;
    public event Action? OnPlayPause;
    public event Action? OnStop;
    public event Action? OnNext;
    public event Action<float>? OnSeek;
    public event Action<float>? OnVolumeChanged;

    private EntityUid? _audio;

    public MusicPlayerWindow()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);
        _audioSystem = _entManager.System<AudioSystem>();

        CategoryList.OnItemSelected += args =>
        {
            if (CategoryList[args.ItemIndex].Metadata is ProtoId<MusicCategoryPrototype> cat)
                OnCategorySelected?.Invoke(cat);
        };

        TrackList.OnItemSelected += args =>
        {
            if (TrackList[args.ItemIndex].Metadata is ProtoId<MusicTrackPrototype> tr)
                OnTrackSelected?.Invoke(tr);
        };

        PlayPause.OnPressed += _ => OnPlayPause?.Invoke();
        Stop.OnPressed += _ => OnStop?.Invoke();
        Next.OnPressed += _ => OnNext?.Invoke();
        Position.OnReleased += _ => OnSeek?.Invoke((float) Position.Value);
        VolumeSlider.OnValueChanged += _ => OnVolumeChanged?.Invoke((float) VolumeSlider.Value);
    }

    public void SetAudioStream(EntityUid? audio)
    {
        _audio = audio;
    }

    public void SetNowPlaying(string text, float length)
    {
        NowPlaying.Text = text;
        Position.MaxValue = length;
        Position.SetValueWithoutEvent(0);
    }

    public void SetCategories(IEnumerable<(string name, ProtoId<MusicCategoryPrototype> id)> categories)
    {
        CategoryList.Clear();
        foreach (var (name, id) in categories)
        {
            CategoryList.AddItem(name, metadata: id);
        }
    }

    public void SetTracks(IEnumerable<(string name, ProtoId<MusicTrackPrototype> id)> tracks)
    {
        TrackList.Clear();
        foreach (var (name, id) in tracks)
        {
            TrackList.AddItem(name, metadata: id);
        }
    }

    protected override void FrameUpdate(Robust.Shared.Timing.FrameEventArgs args)
    {
        base.FrameUpdate(args);

        if (_entManager.TryGetComponent(_audio, out AudioComponent? audio))
        {
            Duration.Text = $"{TimeSpan.FromSeconds(audio.PlaybackPosition):mm\\:ss} / {_audioSystem.GetAudioLength(audio.FileName):mm\\:ss}";
            if (!Position.Grabbed)
                Position.SetValueWithoutEvent(audio.PlaybackPosition);
        }
        else
        {
            Duration.Text = "00:00 / 00:00";
            if (!Position.Grabbed)
                Position.SetValueWithoutEvent(0f);
        }
    }
}
