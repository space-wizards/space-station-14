using System.Linq;
using Content.Client.UserInterface;
using Content.Shared.Ame;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.Ame.UI;

[GenerateTypedNameReferences]
public sealed partial class AmeWindow : DefaultWindow
{
    public event Action? OnAmeEjectButton;
    public event Action? OnAmeToggleInjectionButton;
    public event Action? OnAmeIncreaseFuelButton;
    public event Action? OnAmeDecreaseFuelButton;

    public AmeWindow()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        EjectButton.OnPressed += _ => OnAmeEjectButton?.Invoke();
        ToggleInjection.OnPressed += _ => OnAmeToggleInjectionButton?.Invoke();
        IncreaseFuelButton.OnPressed += _ => OnAmeIncreaseFuelButton?.Invoke();
        DecreaseFuelButton.OnPressed += _ => OnAmeDecreaseFuelButton?.Invoke();
    }

    /// <summary>
    /// Update the UI state when new state data is received from the server.
    /// </summary>
    /// <param name="state">State data sent by the server.</param>
    public void UpdateState(AmeControllerBoundUserInterfaceState state)
    {
        CurrentPowerSupply.Text = $"{state.CurrentPowerSupply:N1}";
    }

    public void SetFuelJar(bool value, int fuelAmount)
    {
        if (!value)
        {
            EjectButton.Disabled = true;
            ToggleInjection.Disabled = true;
            FuelAmount.Text = Loc.GetString("ame-window-fuel-not-inserted-text");
        }
        else
        {
            EjectButton.Disabled = false;
            ToggleInjection.Disabled = false;
            FuelAmount.Text = $"{fuelAmount}";
        }
    }

    public void SetInjectionStatus(bool injecting)
    {
        if (injecting)
            InjectionStatus.Text = Loc.GetString("ame-window-engine-injection-status-injecting-label") + " ";
        else
            InjectionStatus.Text = Loc.GetString("ame-window-engine-injection-status-not-injecting-label") + " ";
    }

    public void SetPowered(bool powered)
    {
        if (!Contents.Children.Any())
            return;

        ButtonHelpers.SetButtonDisabledRecursive(Contents, !powered);
        EjectButton.Disabled = false;
    }

    public void SetIsMaster(bool isMaster)
    {
        ToggleInjection.Disabled = true;
    }

    public void SetAmeStats(int coreCount, int injectionAmount, float targetedPowerSupply)
    {
        CoreCount.Text = $"{coreCount}";
        InjectionAmount.Text = $"{injectionAmount}";
        TargetedPowerSupply.Text = $"{targetedPowerSupply:N1}";
    }
}
