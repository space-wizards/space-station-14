using System.Diagnostics;
using Content.Shared.Preferences;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.Lobby.UI;

/// <summary>
/// A toggle button with a lock icon meant to lock values for randomizing in the character editor screen.
/// </summary>
[GenerateTypedNameReferences]
public sealed partial class RandomizeLockButton : TextureButton
{
    /// <summary>
    /// Which value this lock meant for.
    /// </summary>
    public HumanoidCharacterProfile.RandomizeConfig For { get; set; }

    /// <summary>
    /// Which values to allow to be randomized.
    /// </summary>
    public static HumanoidCharacterProfile.RandomizeConfig RandomizeConfig = HumanoidCharacterProfile.RandomizeConfigAll;

    public RandomizeLockButton()
    {
        RobustXamlLoader.Load(this);

        OnToggled += args => HandleToggle(args.Pressed);
    }

    private void HandleToggle(bool toggle)
    {
        if (For == default)
            return;
        if (toggle)
        {
            // if pressed, disable specific randomize by setting its bit to 0
            RandomizeConfig &= ~For;
        }
        else
        {
            // if not pressed, enable specific randomize by setting its bit to 1
            RandomizeConfig |= For;
        }
    }
}

