using Content.Shared.Administration;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Utility;
using Robust.Shared.Audio.Systems;
using Robust.Shared.Player;
using Robust.Shared.Timing;

namespace Content.Client.Administration.UI
{
    [GenerateTypedNameReferences]
    public sealed partial class AdminAnnounceWindow : DefaultWindow
    {
        [Dependency] private readonly ILocalizationManager _localization = default!;
        [Dependency] private readonly IEntitySystemManager _sysMan = default!;

        private EntityUid? _previewStream;
        private readonly SharedAudioSystem? _audio;

        public AdminAnnounceWindow()
        {
            RobustXamlLoader.Load(this);
            IoCManager.InjectDependencies(this);
            
            _audio = _sysMan.GetEntitySystem<SharedAudioSystem>();

            ColorHex.Text = AdminAnnounceDefaults.DefaultColorHex;
            SoundPath.Text = AdminAnnounceDefaults.DefaultSoundPath;
            UpdateColorPreview();

            Announcement.Placeholder = new Rope.Leaf(_localization.GetString("admin-announce-announcement-placeholder"));
            
            AnnounceMethod.AddItem(_localization.GetString("admin-announce-type-station"));
            AnnounceMethod.SetItemMetadata(0, AdminAnnounceType.Station);
            AnnounceMethod.AddItem(_localization.GetString("admin-announce-type-server"));
            AnnounceMethod.SetItemMetadata(1, AdminAnnounceType.Server);
            
            AnnounceMethod.OnItemSelected += args =>
            {
                AnnounceMethod.SelectId(args.Id);
                UpdateFields((AdminAnnounceType?)args.Button.SelectedMetadata ?? AdminAnnounceType.Station);
            };
            
            AnnounceMethod.SelectId(0);
            UpdateFields(AdminAnnounceType.Station);

            PlayAudio.OnPressed += _ => 
            {
                if (_audio == null) return;

                if (IsStreamPlaying(_previewStream))
                {
                    StopPreview();
                }
                else
                {
                    if (string.IsNullOrWhiteSpace(SoundPath.Text)) return;
                    _previewStream = _audio.PlayGlobal(SoundPath.Text, Filter.Local(), false)?.Entity;
                }
                UpdateButtons();
            };

            AnnounceButton.OnPressed += _ =>
            {
                StopPreview();
                UpdateButtons();
            };

            Announcement.OnKeyBindUp += _ => UpdateButtons();
            ColorHex.OnTextChanged += _ => UpdateColorPreview();
        }

        private void StopPreview()
        {
            if (_previewStream == null) return;
            _audio?.Stop(_previewStream);
            _previewStream = null;
        }

        private static bool IsStreamPlaying(EntityUid? stream)
        {
            if (stream == null || !stream.Value.IsValid())
                return false;

            var entMan = IoCManager.Resolve<IEntityManager>();
            return entMan.EntityExists(stream.Value);
        }

        protected override void FrameUpdate(FrameEventArgs args)
        {
            base.FrameUpdate(args);
            UpdateButtons();
        }

        private void UpdateButtons()
        {
            var isPreviewing = IsStreamPlaying(_previewStream);
            var hasText = !string.IsNullOrWhiteSpace(Rope.Collapse(Announcement.TextRope));
            var type = (AdminAnnounceType?)AnnounceMethod.SelectedMetadata;
            var isStation = type == AdminAnnounceType.Station;

            AnnounceButton.Disabled = !hasText;
            PlayAudio.Disabled = !isStation;
            PlayAudio.Text = isPreviewing ? "⏹" : "▶";
        }

        private void UpdateColorPreview()
        {
            var hex = AdminAnnounceHelpers.CleanHex(ColorHex.Text);
            ColorPreview.ModulateSelfOverride = Color.TryFromHex("#" + hex);
        }

        private void UpdateFields(AdminAnnounceType type)
        {
            var isStation = type == AdminAnnounceType.Station;
            Announcer.Editable = isStation;
            Sender.Editable = isStation;
            SoundPath.Editable = isStation;

            if (isStation)
            {
                ColorHex.Text = AdminAnnounceDefaults.DefaultColorHex;
            }
            else
            {
                ColorHex.Text = AdminAnnounceDefaults.ServerColorHex;
                StopPreview();
            }
            
            UpdateColorPreview();
            UpdateButtons();
        }
    }
}
