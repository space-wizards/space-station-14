using Content.Shared._Offbrand.Analyzers;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;

namespace Content.Client._Offbrand.Analyzer;

[GenerateTypedNameReferences]
public sealed partial class VitalsAnalyzerControl : BoxContainer
{
    public VitalsAnalyzerControl()
    {
    }

    public void Update(VitalsData data)
    {
        SpO2Label.Text = Loc.GetString(data.Spo2Name);
        SpO2Value.Text = Loc.GetString("offbrand-vitals-spo2-value", ("value", $"{data.Spo2 * 100:F0}"));

        BrainActivityValue.Text = Loc.GetString("offbrand-vitals-brain-activity-value", ("value", $"{data.BrainHealth * 100:F0}"));

        RespiratoryRateValue.Text = Loc.GetString("offbrand-vitals-respiratory-rate-value", ("value", data.RespiratoryRate));

        EtCO2Label.Text = Loc.GetString(data.Etco2Name);
        EtCO2Value.Text = Loc.GetString("offbrand-vitals-etco2-value", ("value", data.Etco2));

        var (systolic, diastolic) = data.BloodPressure;
        BloodPressureValue.Text = Loc.GetString("offbrand-vitals-blood-pressure-value", ("systolic", systolic), ("diastolic", diastolic));
        BloodVolumeValue.Text = Loc.GetString("offbrand-vitals-blood-volume-value", ("value", $"{data.BloodLevel * 100:F1}"));

        HeartRateValue.Text = Loc.GetString("offbrand-vitals-heart-rate-value", ("value", data.HeartRate));
    }
}
