        public void SetCurrentName(string name)
        {
            NameLineEdit.Text = name;
            CurrentName.Text = name;
        }

        public void SetCurrentJob(string job)
        {
            JobLineEdit.Text = job;
            CurrentJob.Text = job;
        }
    }
}
