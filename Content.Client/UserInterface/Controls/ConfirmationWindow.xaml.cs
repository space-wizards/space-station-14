using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.UserInterface.Controls;

/// <summary>
/// A generic confirmation window for all your confirmation needs.
/// </summary>
[GenerateTypedNameReferences]
public sealed partial class ConfirmationWindow : FancyWindow
{
    public ConfirmationWindow(string title, string? description = null, string? acceptButton = null, string? cancelButton = null)
    {
        RobustXamlLoader.Load(this);

        Title = title;

        if (description != null)
            Description.Text = description;
        else
            RemoveChild(Description);

        if (acceptButton != null)
            AcceptButton.Text = acceptButton;

        if (cancelButton != null)
            CancelButton.Text = cancelButton;
    }
}
