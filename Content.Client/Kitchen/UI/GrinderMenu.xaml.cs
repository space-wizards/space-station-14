using System.Linq;
using Content.Client.UserInterface.Controls;
using Content.Shared.Chemistry.Reagent;
using Content.Shared.Containers.ItemSlots;
using Content.Shared.Kitchen;
using Content.Shared.Kitchen.Components;
using Content.Shared.Kitchen.EntitySystems;
using Content.Shared.Power.EntitySystems;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Prototypes;

namespace Content.Client.Kitchen.UI;

[GenerateTypedNameReferences]
public sealed partial class GrinderMenu : FancyWindow
{
    [Dependency] private IEntityManager _entityManager = default!;
    [Dependency] private IPrototypeManager _prototypeManager = default!;

    private readonly ItemSlotsSystem _slots;
    private readonly SharedPowerReceiverSystem _power;
    private readonly SharedReagentGrinderSystem _grinder;

    private EntityUid _owner;

    public event Action? OnToggleAuto;
    public event Action? OnGrind;
    public event Action? OnJuice;
    public event Action? OnEjectAll;
    public event Action? OnEjectBeaker;
    public event Action<EntityUid>? OnEjectChamber;

    public GrinderMenu()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);
        _slots = _entityManager.System<ItemSlotsSystem>();
        _power = _entityManager.System<SharedPowerReceiverSystem>();
        _grinder = _entityManager.System<SharedReagentGrinderSystem>();

        AutoModeButton.OnPressed += _ => OnToggleAuto?.Invoke();
        GrindButton.OnPressed += _ => OnGrind?.Invoke();
        JuiceButton.OnPressed += _ => OnJuice?.Invoke();
        ChamberEjectButton.OnPressed += _ => OnEjectAll?.Invoke();
        BeakerEjectButton.OnPressed += _ => OnEjectBeaker?.Invoke();
    }

    public void SetEntity(EntityUid owner)
    {
        _owner = owner;
        UpdateUi();
    }

    public void UpdateUi(ReagentGrinderUpdateUserInterfaceState? state = null)
    {
        var hasServerState = state != null;

        if (state == null)
        {
            if (!_entityManager.TryGetComponent<ReagentGrinderComponent>(_owner, out var grinderComp))
                return;

            state = new ReagentGrinderUpdateUserInterfaceState(
                grinderComp.InputContainer.ContainedEntities.Select(x => _entityManager.GetNetEntity(x)).ToArray(),
                _slots.GetItemOrNull(_owner, ReagentGrinderComponent.BeakerSlotId) is { } b ? _entityManager.GetNetEntity(b) : null,
                _grinder.IsActive((_owner, grinderComp)),
                _power.IsPowered(_owner),
                grinderComp.Program,
                grinderComp.AutoMode,
                new List<ReagentQuantity>(),
                0,
                0
            );
        }

        var chamberEntities = state.ChamberEntities.Select(x => _entityManager.GetEntity(x)).Where(x => x.Valid).ToArray();
        var beaker = state.Beaker.HasValue ? _entityManager.GetEntity(state.Beaker.Value) : (EntityUid?) null;

        var hasInput = chamberEntities.Length > 0;
        var canGrind = hasInput && chamberEntities.All(x => _grinder.CanGrind(x));
        var canJuice = hasInput && chamberEntities.All(x => _grinder.CanJuice(x));

        BeakerEjectButton.Disabled = state.IsActive || !beaker.HasValue;
        ChamberEjectButton.Disabled = state.IsActive || !hasInput;
        ChamberDisabledOverlay.Visible = state.IsActive;
        GrindButton.Disabled = state.IsActive || !canGrind || !state.IsPowered || !beaker.HasValue;
        JuiceButton.Disabled = state.IsActive || !canJuice || !state.IsPowered || !beaker.HasValue;

        GrindButton.Modulate = state.Program == GrinderProgram.Grind ? Color.Green : Color.White;
        JuiceButton.Modulate = state.Program == GrinderProgram.Juice ? Color.Green : Color.White;

        AutoModeButton.Text = state.AutoMode switch
        {
            GrinderAutoMode.Grind => Loc.GetString("grinder-menu-grind-button"),
            GrinderAutoMode.Juice => Loc.GetString("grinder-menu-juice-button"),
            _ => Loc.GetString("grinder-menu-auto-button-off"),
        };

        IngredientGridHelper.PopulateIngredientsGrid(
            ChamberGrid,
            _entityManager,
            chamberEntities,
            netEntity => OnEjectChamber?.Invoke(_entityManager.GetEntity(netEntity)),
            Loc.GetString("grinder-menu-chamber-empty"));

        BeakerContents.Children.Clear();
        BeakerContents.VerticalAlignment = VAlignment.Top;

        var beakerName = beaker.HasValue
            ? _entityManager.GetComponent<MetaDataComponent>(beaker.Value).EntityName
            : Loc.GetString("grinder-menu-no-beaker");

        BeakerNameLabel.Text = beakerName;
        BeakerVolumeLabel.Text = beaker.HasValue && hasServerState
            ? $"{state.CurrentVolume}/{state.MaxVolume}"
            : " ";

        if (!beaker.HasValue)
        {
            BeakerContents.VerticalAlignment = VAlignment.Center;
            BeakerContents.Children.Add(ReagentListHelper.BuildPlaceholderRow(Loc.GetString("grinder-menu-no-beaker"), true));
            return;
        }

        if (hasServerState && state.BeakerReagents.Count == 0)
        {
            BeakerContents.VerticalAlignment = VAlignment.Center;
            BeakerContents.Children.Add(ReagentListHelper.BuildPlaceholderRow(Loc.GetString("grinder-menu-beaker-empty"), true));
            return;
        }

        for (var i = 0; i < state.BeakerReagents.Count; i++)
        {
            var reagent = state.BeakerReagents[i];
            _prototypeManager.TryIndex(reagent.Reagent.Prototype, out ReagentPrototype? proto);
            var reagentName = proto?.LocalizedName ?? Loc.GetString("grinder-menu-unknown-reagent");
            var reagentColor = proto?.SubstanceColor ?? Color.White;

            BeakerContents.Children.Add(ReagentListHelper.BuildReagentRow(reagentName, reagent.Quantity, reagentColor, i));
        }
    }
}
