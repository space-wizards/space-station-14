using Content.Shared.Cargo;
using Content.Shared.Cargo.Prototypes;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.Cargo.UI
{
    [GenerateTypedNameReferences]
    public sealed partial class CargoBasketRow : BoxContainer
    {
        public CargoProductPrototype? Product { get; set; }
        private Func<int, bool>? IsValid { get; set; }

        public CargoBasketRow()
        {
            RobustXamlLoader.Load(this);
            Amount.SetButtons(new List<int> { -1 }, new List<int> { 1 });
            Amount.IsValid = n => n <= 100 && n > 0;
        }
    }
}
