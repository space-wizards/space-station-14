using Content.Shared.RussStation.Botany;
using Robust.Client.AutoGenerated;
using Robust.Client.GameObjects;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Utility;
using FancyWindow = Content.Client.UserInterface.Controls.FancyWindow;

namespace Content.Client.RussStation.Botany.UI;

[GenerateTypedNameReferences]
public sealed partial class SeedExtractorStorageMenu : FancyWindow
{
    public event Action<string>? OnTakePressed;

    private readonly SpriteSystem _spriteSystem;
    private List<SeedExtractorStorageSeedData> _seeds = new();

    public SeedExtractorStorageMenu()
    {
        RobustXamlLoader.Load(this);
        _spriteSystem = IoCManager.Resolve<IEntitySystemManager>().GetEntitySystem<SpriteSystem>();

        SearchBar.OnTextChanged += _ => RebuildList();
    }

    public void Populate(List<SeedExtractorStorageSeedData> seeds)
    {
        _seeds = seeds;
        RebuildList();
    }

    private void RebuildList()
    {
        SeedList.Children.Clear();

        var search = SearchBar.Text.Trim().ToLowerInvariant();

        foreach (var seed in _seeds)
        {
            if (search.Length > 0 && !seed.DisplayName.ToLowerInvariant().Contains(search))
                continue;

            SeedList.AddChild(BuildRow(seed));
        }
    }

    private Control BuildRow(SeedExtractorStorageSeedData seed)
    {
        var row = new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Horizontal,
            HorizontalExpand = true,
            Margin = new Thickness(4, 2),
        };

        var icon = new TextureRect
        {
            Texture = _spriteSystem.Frame0(new SpriteSpecifier.EntityPrototype(seed.PacketPrototype)),
            Stretch = TextureRect.StretchMode.KeepAspectCentered,
            SetSize = new System.Numerics.Vector2(24, 24),
            Margin = new Thickness(0, 0, 4, 0),
            VerticalAlignment = VAlignment.Center,
        };
        row.AddChild(icon);

        row.AddChild(new Label
        {
            Text = $"{seed.Count}x {seed.DisplayName}",
            HorizontalExpand = true,
            ClipText = true,
            Margin = new Thickness(0, 0, 16, 0),
        });

        row.AddChild(StatLabel(seed.Potency.ToString("0"), 50));
        row.AddChild(StatLabel(seed.Yield.ToString("0"), 50));
        row.AddChild(StatLabel(seed.Maturation.ToString("0"), 50));
        row.AddChild(StatLabel(seed.Production.ToString("0"), 50));
        row.AddChild(StatLabel(seed.Lifespan.ToString("0"), 50));
        row.AddChild(StatLabel(seed.Endurance.ToString("0"), 50));

        var takeButton = new Button
        {
            Text = Loc.GetString("seed-extractor-menu-take"),
            MinWidth = 65,
        };
        takeButton.OnPressed += _ => OnTakePressed?.Invoke(seed.GroupKey);
        row.AddChild(takeButton);

        return row;
    }

    private static Label StatLabel(string text, float minWidth)
    {
        return new Label
        {
            Text = text,
            MinWidth = minWidth,
            HorizontalAlignment = HAlignment.Center,
        };
    }
}
