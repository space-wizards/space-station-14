using System.Text;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Timing;
using Content.Shared.Forensics;

namespace Content.Client.Forensics
{
    [GenerateTypedNameReferences]
    public sealed partial class ForensicScannerMenu : DefaultWindow
    {
        [Dependency] private readonly IGameTiming _gameTiming = default!;

        public ForensicScannerMenu()
        {
            RobustXamlLoader.Load(this);
            IoCManager.InjectDependencies(this);
        }

        public void UpdatePrinterState(bool disabled)
        {
            Print.Disabled = disabled;
        }

        public void UpdateState(ForensicScannerBoundUserInterfaceState msg)
        {
            if (string.IsNullOrEmpty(msg.LastScannedName))
            {
                Print.Disabled = true;
                Clear.Disabled = true;
                NameLabel.Text = string.Empty;
                Diagnostics.Text = string.Empty;
                return;
            }

            Print.Disabled = (msg.PrintReadyAt > _gameTiming.CurTime);
            Clear.Disabled = false;

            NameLabel.Text = msg.LastScannedName;

            var text = new StringBuilder();

            if (msg.Evidence.Count == 0)
                text.AppendLine(Loc.GetString("forensic-scanner-interface-empty"));
            else
            {
                foreach (var type in Enum.GetValues<ForensicEvidence>())
                {
                    text.AppendLine(Loc.GetString($"forensic-scanner-interface-{type.ToString().ToLower()}"));
                    foreach (var evidence in msg.Evidence[type])
                    {
                        text.AppendLine(evidence);
                    }
                    text.AppendLine();
                }
                Diagnostics.Text = text.ToString();
            }
        }
    }
}
