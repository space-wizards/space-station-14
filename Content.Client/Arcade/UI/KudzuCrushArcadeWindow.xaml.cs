using Content.Client.UserInterface.Controls;
using Content.Shared.Arcade.Enums;
using Content.Shared.Input;
using Robust.Client.AutoGenerated;
using Robust.Client.GameObjects;
using Robust.Client.Graphics;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Utility;

namespace Content.Client.Arcade.UI;

/// <summary>
///
/// </summary>
[GenerateTypedNameReferences]
public sealed partial class KudzuCrushArcadeWindow : FancyWindow
{
    [Dependency] private readonly IEntitySystemManager _entitySystem = default!;

    /// <summary>
    ///
    /// </summary>
    private static readonly Vector2i CellSize = new(32, 32);

    /// <summary>
    ///
    /// </summary>
    private readonly StyleBoxTexture _blockStyle;

    /// <summary>
    ///
    /// </summary>
    private readonly StyleBoxTexture _kudzuStyle;

    /// <summary>
    ///
    /// </summary>
    private static readonly StyleBoxEmpty EmptyStyle = new();

    /// <summary>
    ///
    /// </summary>
    public event Action<KudzuCrushArcadeAction>? OnAction;

    /// <summary>
    ///
    /// </summary>
    private bool _usability = true;

    public KudzuCrushArcadeWindow()
    {
        RobustXamlLoader.Load(this);

        var spriteSystem = _entitySystem.GetEntitySystem<SpriteSystem>();

        _blockStyle = new()
        {
            Texture = spriteSystem.Frame0(new SpriteSpecifier.Rsi(new("Structures/Walls/solid.rsi"), "rgeneric")),
        };

        _kudzuStyle = new()
        {
            Texture = spriteSystem.Frame0(new SpriteSpecifier.Rsi(new("Objects/Misc/kudzu.rsi"), "kudzu_33")),
        };

        GrabKeyboardFocus();
    }

    protected override void KeyBindDown(GUIBoundKeyEventArgs args)
    {
        base.KeyBindDown(args);

        if (!_usability || args.Handled)
            return;

        if (!HasKeyboardFocus())
            return;

        if (args.Function == ContentKeyFunctions.ArcadeLeft)
        {
            OnAction?.Invoke(KudzuCrushArcadeAction.Left);
            args.Handle();
        }
        else if (args.Function == ContentKeyFunctions.ArcadeRight)
        {
            OnAction?.Invoke(KudzuCrushArcadeAction.Right);
            args.Handle();
        }
        else if (args.Function == ContentKeyFunctions.ArcadeDown)
        {
            OnAction?.Invoke(KudzuCrushArcadeAction.Drop);
            args.Handle();
        }
        else if (args.Function == ContentKeyFunctions.ArcadeRotate)
        {
            OnAction?.Invoke(KudzuCrushArcadeAction.Rotate);
            args.Handle();
        }
    }

    /// <summary>
    ///
    /// </summary>
    public void SetUsability(bool value)
    {
        _usability = value;

        NewGameButton.Disabled = !value;
    }

    /// <summary>
    ///
    /// </summary>
    public void UpdateGrid(int gridWidth, KudzuCrushArcadeCell[] grid)
    {
        Grid.Columns = gridWidth;

        for (var i = 0; i < grid.Length; i++)
        {
            if (i < Grid.ChildCount && Grid.Children[i] is PanelContainer { } panel)
            {
                panel.PanelOverride = CellToStyle(grid[i]);
                continue;
            }

            Grid.AddChild(new PanelContainer
            {
                PanelOverride = CellToStyle(grid[i]),
                MinSize = CellSize,
            });
        }
    }

    /// <summary>
    ///
    /// </summary>
    private StyleBox CellToStyle(KudzuCrushArcadeCell cell)
    {
        return cell switch
        {
            KudzuCrushArcadeCell.Block => _blockStyle,
            KudzuCrushArcadeCell.Kudzu => _kudzuStyle,
            _ => EmptyStyle,
        };
    }
}
