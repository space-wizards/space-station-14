using Content.Shared.Atmos;
using Content.Shared.Atmos.EntitySystems;
using Content.Shared.Atmos.Monitor;
using Content.Shared.Atmos.Piping.Unary.Components;
using Content.Shared.Atmos.Prototypes;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Prototypes;

namespace Content.Client.Atmos.Monitor.UI.Widgets;

[GenerateTypedNameReferences]
public sealed partial class ScrubberControl : BoxContainer
{
    [Dependency] private IPrototypeManager _prototypeManager = default!;
    [Dependency] private IEntityManager _entMan = default!;

    private GasVentScrubberDataPayload _dataPayload;
    private string _address;

    public event Action<string, AtmosDeviceDataPayload>? ScrubberDataChanged;
	public event Action<AtmosDeviceDataPayload>? ScrubberDataCopied;

    private CheckBox _enabled => CEnableDevice;
    private CollapsibleHeading _addressLabel => CAddress;
    private OptionButton _pumpDirection => CPumpDirection;
    private FloatSpinBox _volumeRate => CVolumeRate;
    private CheckBox _wideNet => CWideNet;
    private Button _copySettings => CCopySettings;
    private Button _selectAll => CSelectAll;
    private Button _deselectAll => CDeselectAll;

    private GridContainer _gases => CGasContainer;
    private Dictionary<Gas, Button> _gasControls = new();

    public ScrubberControl(GasVentScrubberDataPayload dataPayload, string address)
    {

        IoCManager.InjectDependencies(this);
        var atmosphereSystem = _entMan.System<SharedAtmosphereSystem>();

        RobustXamlLoader.Load(this);

        Name = address;

        _dataPayload = dataPayload;
        _address = address;

        _addressLabel.Title = Loc.GetString("air-alarm-ui-atmos-net-device-label", ("address", $"{address}"));

        _enabled.Pressed = dataPayload.Enabled;
        _enabled.OnToggled += _ =>
        {
            _dataPayload.Enabled = _enabled.Pressed;
            ScrubberDataChanged?.Invoke(_address, _dataPayload);
        };

        _wideNet.Pressed = dataPayload.WideNet;
        _wideNet.OnToggled += _ =>
        {
            _dataPayload.WideNet = _wideNet.Pressed;
            ScrubberDataChanged?.Invoke(_address, _dataPayload);
        };

        _volumeRate.Value = _dataPayload.VolumeRate;
        _volumeRate.OnValueChanged += _ =>
        {
            _dataPayload.VolumeRate = _volumeRate.Value;
            ScrubberDataChanged?.Invoke(_address, _dataPayload);
        };
        _volumeRate.IsValid += value => value >= 0;

        foreach (var value in Enum.GetValues<ScrubberPumpDirection>())
        {
            _pumpDirection.AddItem(Loc.GetString($"air-alarm-ui-pump-direction-{value.ToString().ToLower()}"), (int) value);
        }

        _pumpDirection.SelectId((int) _dataPayload.PumpDirection);
        _pumpDirection.OnItemSelected += args =>
        {
            _pumpDirection.SelectId(args.Id);
            _dataPayload.PumpDirection = (ScrubberPumpDirection) args.Id;
            ScrubberDataChanged?.Invoke(_address, _dataPayload);
        };
        _pumpDirection.Disabled = dataPayload.AirAlarmPanicWireCut;

        _copySettings.OnPressed += _ =>
        {
            ScrubberDataCopied?.Invoke(_dataPayload);
        };

        var allGases = Enum.GetValues<Gas>();
        _selectAll.OnPressed += _ =>
        {
            _dataPayload.FilterGases = new HashSet<Gas>(allGases);
            ScrubberDataChanged?.Invoke(_address, _dataPayload);
        };

        _deselectAll.OnPressed += _ =>
        {
            _dataPayload.FilterGases = [];
            ScrubberDataChanged?.Invoke(_address, _dataPayload);
        };

        foreach (var value in allGases)
        {
            ProtoId<GasPrototype> gasProtoId = atmosphereSystem.GetGas(value);
            var gasName = _prototypeManager.Index(gasProtoId).Name;

            var gasButton = new Button
            {
                Name = value.ToString(),
                Text = Loc.GetString(gasName),
                ToggleMode = true,
                HorizontalExpand = true,
                Pressed = _dataPayload.FilterGases.Contains(value)
            };
            gasButton.OnToggled += args =>
            {
                if (args.Pressed)
                    _dataPayload.FilterGases.Add(value);
                else
                    _dataPayload.FilterGases.Remove(value);

                ScrubberDataChanged?.Invoke(_address, _dataPayload);
            };
            _gasControls.Add(value, gasButton);
            _gases.AddChild(gasButton);
        }

    }

    public void ChangeData(GasVentScrubberDataPayload dataPayload)
    {
        _dataPayload.Enabled = dataPayload.Enabled;
        _enabled.Pressed = _dataPayload.Enabled;

        _dataPayload.PumpDirection = dataPayload.PumpDirection;
        _pumpDirection.Select((int) _dataPayload.PumpDirection);
        _pumpDirection.Disabled = dataPayload.AirAlarmPanicWireCut;

        _dataPayload.VolumeRate = dataPayload.VolumeRate;
        _volumeRate.Value = _dataPayload.VolumeRate;

        _dataPayload.WideNet = dataPayload.WideNet;
        _wideNet.Pressed = _dataPayload.WideNet;
        _dataPayload.FilterGases = dataPayload.FilterGases;

        foreach (var value in Enum.GetValues<Gas>())
        {
            _gasControls[value].Pressed = dataPayload.FilterGases.Contains(value);
        }
    }
}
