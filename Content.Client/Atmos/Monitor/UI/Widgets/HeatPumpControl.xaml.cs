using Content.Client.Message;
using Content.Shared.Atmos.Monitor.Components;
using Content.Shared.Atmos.Piping.Binary.Components;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.Atmos.Monitor.UI.Widgets;

[GenerateTypedNameReferences]
public sealed partial class HeatPumpControl : BoxContainer
{
    private GasHeatPumpData _data;
    private string _address;

    public event Action<string, IAtmosDeviceData>? HeatPumpDataChanged;
    public event Action<IAtmosDeviceData>? HeatPumpDataCopied;

    private CheckBox _enabled => CEnableDevice;
    private CollapsibleHeading _addressLabel => CAddress;
    private FloatSpinBox _targetTemp => CTargetTemp;
    private Button _copySettings => CCopySettings;

    public HeatPumpControl(GasHeatPumpData data, string address)
    {
        RobustXamlLoader.Load(this);

        Name = address;
        _data = data;
        _address = address;

        _addressLabel.Title = Loc.GetString("air-alarm-ui-atmos-net-device-label", ("address", $"{address}"));

        _enabled.Pressed = data.Enabled;
        _enabled.OnToggled += _ =>
        {
            _data.Enabled = _enabled.Pressed;
            HeatPumpDataChanged?.Invoke(_address, _data);
        };

        _targetTemp.Value = Math.Clamp(data.TargetTemperature, data.MinOperatingTemperature, data.MaxOperatingTemperature);
        _targetTemp.OnValueChanged += _ =>
        {
            // Under min / Over max gets clamped
            var clamped = Math.Clamp(_targetTemp.Value, _data.MinOperatingTemperature, _data.MaxOperatingTemperature);
            if (clamped != _targetTemp.Value)
                _targetTemp.Value = clamped;

            _data.TargetTemperature = clamped;
            HeatPumpDataChanged?.Invoke(_address, _data);
        };

        _copySettings.OnPressed += _ =>
        {
            HeatPumpDataCopied?.Invoke(_data);
        };
    }

    public void ChangeData(GasHeatPumpData data)
    {
        _data.Enabled = data.Enabled;
        _enabled.Pressed = _data.Enabled;

        _data.MinOperatingTemperature = data.MinOperatingTemperature;
        _data.MaxOperatingTemperature = data.MaxOperatingTemperature;
        _data.TargetTemperature = Math.Clamp(data.TargetTemperature, data.MinOperatingTemperature, data.MaxOperatingTemperature);
        _targetTemp.Value = _data.TargetTemperature;
    }
}
