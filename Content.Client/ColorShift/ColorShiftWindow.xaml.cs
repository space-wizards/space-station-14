using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.CustomControls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.ColorShift;

[GenerateTypedNameReferences]
public sealed partial class ColorShiftWindow : DefaultWindow
{
    public event Action<Color>? OnHueShift;
    private Color _selectedColor = Color.Black;

    public ColorShiftWindow()
    {
        RobustXamlLoader.Load(this);

        ColorSelector.OnColorChanged = OnColorChanged;
        HueshiftButton.OnPressed += _ => SubmitHueshift();
    }

    private void SubmitHueshift()
    {
        OnHueShift!.Invoke(_selectedColor);
    }

    private void OnColorChanged(Color color)
    {
        _selectedColor = color;
    }

    public void SetCurrentColor(Color color)
    {
        ColorSelector.Color = color;
        _selectedColor = color;
    }
}
